languages = ['Java', 'Python', 'JavaScript', 'HTML', 'CSS', 'Markdown']

print("Sorting Permanently:")
languages.sort()
print(languages)

print("\nSorting Temporarily:")
print(sorted(languages))

print("\nSorting in Reverse Order:")
languages.reverse()
print(languages)

print("\nLength of List:")
print(len(languages))
